function curl_get_contents($url) {
    
        // this function was found on http://fusionswift.com/blog/2010/02/curl-vs-file_get_contents/
        
	// Initiate the curl session
	$ch = curl_init();
	// Set the URL
	curl_setopt($ch, CURLOPT_URL, $url);
	// Removes the headers from the output
	curl_setopt($ch, CURLOPT_HEADER, 0);
	// Return the output instead of displaying it directly
	curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
	// Execute the curl session
	$output = curl_exec($ch);
	// Close the curl session
	curl_close($ch);
	// Return the output as a variable
	return $output;
}

function get_fb_info($username){
    
    // should I check somethings here?
    
    $data = curl_get_contents('http://graph.facebook.com/'.$username);    
    $data = json_decode($data, TRUE);
    
    if( ! isset($data['error']) ){
        
        $general_information = array(

            'id' => $data['id'],
            'link' => $data['link'],
            'username' => $data['username'],
            'name' => $data['name']

        );

        if ( isset($data['likes'] )) {

            $other_information = array(

                'type' => 'page',
                'likes' => $data['likes'],
                'category' => $data['category'],
                'website' => $data['website'],
                'founded' => $data['founded'],
                'likes' => $data['likes'],
            );

        } else {

            $humanreadable = get_humanreadable($data['gender'], $data['locale']);
            
            $other_information = array(

                'type' => 'user',
                'first_name' => $data['first_name'],
                'last_name' => $data['last_name'],
                'gender' => $humanreadable['gender'],
                'locale' => $humanreadable['locale']
                
            );
        }

        $data = array(
            
            'general_information' => $general_information,
            'other_information' => $other_information
            
        );
        
        return $data;
        
    } else {
        
        return FALSE;
        
    }
    
}

